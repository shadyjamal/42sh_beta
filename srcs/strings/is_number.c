#include "shell.h"

int		is_number(char *str)
{
	if (*str == '-' || *str == '+')
		str++;
	while (*str && !ft_isspace(*str))
	{
		if (!ft_isdigit(*str))
			return (0);
		str++;
	}
	return (1);
}
